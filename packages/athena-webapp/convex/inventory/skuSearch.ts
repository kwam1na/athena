async function requireProductSkuSearchAccess(
  ctx: AccessCtx,
  storeId: Id<"store">,
  failureMessage: string,
  allowedRoles: Array<"full_admin" | "pos_only">,
) {
  const store = await ctx.db.get("store", storeId);
  if (!store) {
    throw new Error("Store not found.");
  }

  const athenaUser = await requireAuthenticatedAthenaUserWithCtx(ctx);
  await requireOrganizationMemberRoleWithCtx(ctx, {
    allowedRoles,
    failureMessage,
    organizationId: store.organizationId,
    userId: athenaUser._id,
  });

  return { athenaUser, store };
}

function normalizeLookup(value: string | undefined): string | undefined {
  const normalized = value?.trim().toLowerCase();
  return normalized ? normalized : undefined;
}

function capped(value: string | undefined, maxLength = ATTRIBUTE_VALUE_MAX_LENGTH) {
  if (!value) return undefined;
  const trimmed = value.trim();
  if (!trimmed) return undefined;
  return trimmed.slice(0, maxLength);
}

function normalizeAttributes(
  attributes: Doc<"productSku">["attributes"],
): ProductSkuSearchProjection["attributes"] {
  if (!attributes) return undefined;

  const normalized: NonNullable<ProductSkuSearchProjection["attributes"]> = {};
  for (const [key, value] of Object.entries(attributes)) {
    if (typeof value === "string") {
      normalized[key] = value.slice(0, ATTRIBUTE_VALUE_MAX_LENGTH);
    } else if (
      typeof value === "number" ||
      typeof value === "boolean" ||
      value === null
    ) {
      normalized[key] = value;
    }
  }

  return Object.keys(normalized).length > 0 ? normalized : undefined;
}

function buildSearchText(args: {
  attributes?: ProductSkuSearchProjection["attributes"];
  category?: Doc<"category"> | null;
  color?: Doc<"color"> | null;
  product: Doc<"product">;
  sku: Doc<"productSku">;
  subcategory?: Doc<"subcategory"> | null;
}) {
  const tokens = [
    args.sku.sku,
    args.sku.barcode,
    args.sku.productName,
    args.product.name,
    args.product.slug,
    args.product.description,
    args.category?.name,
    args.category?.slug,
    args.subcategory?.name,
    args.subcategory?.slug,
    args.color?.name,
    args.color?.hexCode,
    args.sku.size,
    args.sku.weight,
    args.sku.length === undefined ? undefined : String(args.sku.length),
    ...(args.attributes
      ? Object.entries(args.attributes).flatMap(([key, value]) => [
          key,
          value === null ? undefined : String(value),
        ])
      : []),
  ];

  return tokens
    .flatMap((token) => capped(token)?.split(/\s+/) ?? [])
    .join(" ")
    .slice(0, SEARCH_TEXT_MAX_LENGTH);
}

export async function buildProductSkuSearchProjection(
  ctx: DatabaseCtx,
  productSkuId: Id<"productSku">,
): Promise<ProductSkuSearchProjection | null> {
  const sku = await ctx.db.get("productSku", productSkuId);
  if (!sku) return null;

  const product = await ctx.db.get("product", sku.productId);
  if (!product || product.storeId !== sku.storeId) {
    return null;
  }

  const [category, subcategory, color] = await Promise.all([
    ctx.db.get("category", product.categoryId),
    ctx.db.get("subcategory", product.subcategoryId),
    sku.color ? ctx.db.get("color", sku.color) : Promise.resolve(null),
  ]);
  const storeCategory = category?.storeId === sku.storeId ? category : null;
  const storeSubcategory =
    subcategory?.storeId === sku.storeId &&
    storeCategory &&
    subcategory.categoryId === storeCategory._id
      ? subcategory
      : null;
  const storeColor = color?.storeId === sku.storeId ? color : null;
  const attributes = normalizeAttributes(sku.attributes);
  const now = Date.now();

  return {
    attributes,
    barcode: sku.barcode,
    barcodeAutoGenerated: sku.barcodeAutoGenerated,
    categoryDescription: storeCategory?.description,
    categoryId: storeCategory?._id,
    categoryName: storeCategory?.name,
    categorySlug: storeCategory?.slug,
    categoryShowOnStorefront: storeCategory?.showOnStorefront,
    colorHexCode: storeColor?.hexCode,
    colorId: storeColor?._id,
    colorName: storeColor?.name,
    currency: product.currency,
    images: sku.images,
    inventoryCount: sku.inventoryCount,
    isVisible: sku.isVisible,
    length: sku.length,
    netPrice: sku.netPrice,
    normalizedBarcode: normalizeLookup(sku.barcode),
    normalizedSku: normalizeLookup(sku.sku),
    price: sku.price,
    productAvailability: product.availability,
    productCategoryId: product.categoryId,
    productDescription: product.description,
    productId: product._id,
    productInventoryCount: product.inventoryCount,
    productIsVisible: product.isVisible,
    productName: product.name,
    productProcessingFeesAbsorbed: product.areProcessingFeesAbsorbed,
    productQuantityAvailable: product.quantityAvailable,
    productSlug: product.slug,
    productSkuCreationTime: sku._creationTime,
    productSkuId: sku._id,
    productSubcategoryId: product.subcategoryId,
    quantityAvailable: sku.quantityAvailable,
    searchText: buildSearchText({
      attributes,
      category: storeCategory,
      color: storeColor,
      product,
      sku,
      subcategory: storeSubcategory,
    }),
    size: sku.size,
    sku: sku.sku,
    storeId: sku.storeId,
    subcategoryDescription: storeSubcategory?.description,
    subcategoryId: storeSubcategory?._id,
    subcategoryName: storeSubcategory?.name,
    subcategorySlug: storeSubcategory?.slug,
    unitCost: sku.unitCost,
    weight: sku.weight,
    sourceUpdatedAt: now,
    updatedAt: now,
  };
}

function projectionsEqual(
  existing: Doc<"productSkuSearch">,
  next: ProductSkuSearchProjection,
) {
  const {
    _id: _existingId,
    _creationTime: _existingCreationTime,
    sourceUpdatedAt: _existingSourceUpdatedAt,
    updatedAt: _existingUpdatedAt,
    ...existingComparable
  } = existing;
  const {
    sourceUpdatedAt: _nextSourceUpdatedAt,
    updatedAt: _nextUpdatedAt,
    ...nextComparable
  } = next;

  return JSON.stringify(existingComparable) === JSON.stringify(nextComparable);
}

export async function upsertProductSkuSearchProjection(
  ctx: Pick<MutationCtx, "db">,
  productSkuId: Id<"productSku">,
): Promise<"upserted" | "unchanged" | "source_orphan"> {
  const projection = await buildProductSkuSearchProjection(ctx, productSkuId);
  const existingRows = await ctx.db
    .query("productSkuSearch")
    .withIndex("by_productSkuId", (q) => q.eq("productSkuId", productSkuId))
    .collect();

  if (!projection) {
    await Promise.all(
      existingRows.map((row) => ctx.db.delete("productSkuSearch", row._id)),
    );
    return "source_orphan";
  }

  const [firstRow, ...duplicates] = existingRows;
  await Promise.all(
    duplicates.map((row) => ctx.db.delete("productSkuSearch", row._id)),
  );

  if (!firstRow) {
    await ctx.db.insert("productSkuSearch", projection);
    return "upserted";
  }

  if (projectionsEqual(firstRow, projection) && duplicates.length === 0) {
    return "unchanged";
  }

  await ctx.db.patch("productSkuSearch", firstRow._id, projection);
  return "upserted";
}

