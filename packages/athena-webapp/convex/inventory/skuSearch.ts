async function hydrateCanonicalProjection(
  ctx: Pick<QueryCtx, "db">,
  candidate: Pick<Doc<"productSkuSearch">, "productSkuId" | "storeId">,
) {
  if (!candidate.productSkuId) return null;

  const projection = await buildProductSkuSearchProjection(
    ctx,
    candidate.productSkuId,
  );

  if (!projection || projection.storeId !== candidate.storeId) {
    return null;
  }

  return projection;
}

export const searchProductSkus = query({
  args: {
    limit: v.optional(v.number()),
    query: v.string(),
    storeId: v.id("store"),
  },
  returns: productSkuSearchResponseValidator,
  handler: async (ctx, args) => {
    await requireProductSkuSearchReadAccess(
      ctx,
      args.storeId,
      "You do not have access to search product SKUs.",
    );

    const queryText = args.query.trim();
    const normalizedQuery = normalizeLookup(queryText);
    const limit = Math.min(Math.max(1, args.limit ?? DEFAULT_LIMIT), MAX_LIMIT);

    if (!normalizedQuery) {
      return {
        candidateOverflow: false,
        limit,
        results: [],
        truncated: false,
      };
    }

    const canonicalSkuLookupValues = [
      queryText,
      queryText.toUpperCase(),
      queryText.toLowerCase(),
    ].filter((value, index, values) => values.indexOf(value) === index);

    const exactCandidates = await Promise.all([
      (async () => {
        const productSkuId = ctx.db.normalizeId("productSku", queryText);
        if (!productSkuId) return [];

        const sku = await ctx.db.get("productSku", productSkuId);
        if (!sku || sku.storeId !== args.storeId) return [];

        return [
          {
            _creationTime: sku._creationTime,
            productSkuId: sku._id,
            storeId: sku.storeId,
          },
        ];
      })(),
      (async () => {
        const matches = await Promise.all(
          canonicalSkuLookupValues.map((lookupValue) =>
            ctx.db
              .query("productSku")
              .withIndex("by_storeId_sku", (q) =>
                q.eq("storeId", args.storeId).eq("sku", lookupValue),
              )
              .take(EXACT_READ_LIMIT),
          ),
        );

        return matches.flat();
      })(),
      ctx.db
        .query("productSku")
        .withIndex("by_storeId_barcode", (q) =>
          q.eq("storeId", args.storeId).eq("barcode", queryText),
        )
        .take(EXACT_READ_LIMIT),
      ctx.db
        .query("productSkuSearch")
        .withIndex("by_storeId_productSkuId", (q) =>
          q
            .eq("storeId", args.storeId)
            .eq("productSkuId", queryText as Id<"productSku">),
        )
        .take(EXACT_READ_LIMIT),
      ctx.db
        .query("productSkuSearch")
        .withIndex("by_storeId_sku", (q) =>
          q.eq("storeId", args.storeId).eq("normalizedSku", normalizedQuery),
        )
        .take(EXACT_READ_LIMIT),
      ctx.db
        .query("productSkuSearch")
        .withIndex("by_storeId_barcode", (q) =>
          q
            .eq("storeId", args.storeId)
            .eq("normalizedBarcode", normalizedQuery),
        )
        .take(EXACT_READ_LIMIT),
    ]);

    const textCandidates = await ctx.db
      .query("productSkuSearch")
      .withSearchIndex("searchText", (q) =>
        q.search("searchText", queryText).eq("storeId", args.storeId),
      )
      .take(TEXT_CANDIDATE_READ_LIMIT);

    const candidateRows = [
      ...exactCandidates[0].map((row) => ({
        row,
        match: {
          kind: "productSkuId" as const,
          matchedValue: String(row.productSkuId),
          rank: 0,
        },
      })),
      ...exactCandidates[1].map((row) => ({
        row: {
          productSkuId: row._id,
          storeId: row.storeId,
        },
        match: {
          kind: "sku" as const,
          matchedValue: row.sku ?? null,
          rank: 1,
        },
      })),
      ...exactCandidates[2].map((row) => ({
        row: {
          productSkuId: row._id,
          storeId: row.storeId,
        },
        match: {
          kind: "barcode" as const,
          matchedValue: row.barcode ?? null,
          rank: 2,
        },
      })),
      ...exactCandidates[3].map((row) => ({
        row,
        match: {
          kind: "productSkuId" as const,
          matchedValue: String(row.productSkuId),
          rank: 0,
        },
      })),
      ...exactCandidates[4].map((row) => ({
        row,
        match: {
          kind: "sku" as const,
          matchedValue: row.sku ?? null,
          rank: 1,
        },
      })),
      ...exactCandidates[5].map((row) => ({
        row,
        match: {
          kind: "barcode" as const,
          matchedValue: row.barcode ?? null,
          rank: 2,
        },
      })),
      ...textCandidates.map((row, index) => ({
        row,
        match: {
          kind: "text" as const,
          matchedValue: queryText,
          rank: 3 + index,
        },
      })),
    ];
    const results: Array<ReturnType<typeof mapProjectionToResult>> = [];
    const seenSkuIds = new Set<string>();

    for (const candidate of candidateRows) {
      const row = candidate.row;
      const key = String(row.productSkuId);
      if (seenSkuIds.has(key)) continue;

      const projection = await hydrateCanonicalProjection(ctx, row);
      if (!projection) continue;

      seenSkuIds.add(key);
      results.push(mapProjectionToResult(projection, candidate.match));
    }

    const truncated = results.length > limit;
    const candidateOverflow = textCandidates.length >= TEXT_CANDIDATE_READ_LIMIT;

    return {
      candidateOverflow,
      limit,
      results: results.slice(0, limit),
      truncated,
    };
  },
});

export const repairProductSkuSearchPage = mutation({
  args: {
    paginationOpts: paginationOptsValidator,
    storeId: v.id("store"),
  },
  returns: repairProductSkuSearchResultValidator,
  handler: async (ctx, args) => {
    await requireProductSkuSearchAdmin(
      ctx,
      args.storeId,
      "You do not have access to repair product SKU search.",
    );

    const page = await ctx.db
      .query("productSku")
      .withIndex("by_storeId", (q) => q.eq("storeId", args.storeId))
      .paginate({
        ...args.paginationOpts,
        numItems: Math.min(args.paginationOpts.numItems, 100),
      });

    const stats = {
      duplicatesCollapsed: 0,
      scanned: page.page.length,
      sourceOrphans: 0,
      staleOrphansRemoved: 0,
      unchanged: 0,
      upserted: 0,
    };

    for (const sku of page.page) {
      const existingRows = await ctx.db
        .query("productSkuSearch")
        .withIndex("by_productSkuId", (q) => q.eq("productSkuId", sku._id))
        .collect();
      const result = await upsertProductSkuSearchProjection(ctx, sku._id);
      if (result === "upserted") stats.upserted += 1;
      if (result === "unchanged") stats.unchanged += 1;
      if (result === "source_orphan") stats.sourceOrphans += 1;
      stats.duplicatesCollapsed += Math.max(0, existingRows.length - 1);
    }

    return {
      ...stats,
      continueCursor: page.continueCursor,
      isDone: page.isDone,
    };
  },
});

export const removeStaleProductSkuSearchPage = mutation({
  args: {
    paginationOpts: paginationOptsValidator,
    storeId: v.id("store"),
  },
  returns: repairProductSkuSearchResultValidator,
  handler: async (ctx, args) => {
    await requireProductSkuSearchAdmin(
      ctx,
      args.storeId,
      "You do not have access to repair product SKU search.",
    );

    const page = await ctx.db
      .query("productSkuSearch")
      .withIndex("by_storeId", (q) => q.eq("storeId", args.storeId))
      .paginate({
        ...args.paginationOpts,
        numItems: Math.min(args.paginationOpts.numItems, 100),
      });

    const stats = {
      duplicatesCollapsed: 0,
      scanned: page.page.length,
      sourceOrphans: 0,
      staleOrphansRemoved: 0,
      unchanged: 0,
      upserted: 0,
    };

    for (const row of page.page) {
      const source = await ctx.db.get("productSku", row.productSkuId);

      if (!source || source.storeId !== args.storeId) {
        await ctx.db.delete("productSkuSearch", row._id);
        stats.sourceOrphans += 1;
        stats.staleOrphansRemoved += 1;
        continue;
      }

      const duplicates = await ctx.db
        .query("productSkuSearch")
        .withIndex("by_productSkuId", (q) =>
          q.eq("productSkuId", row.productSkuId),
        )
        .collect();
      const [canonicalRow, ...duplicateRows] = duplicates.sort((left, right) =>
        left._creationTime - right._creationTime,
      );

      if (canonicalRow && canonicalRow._id !== row._id) {
        stats.unchanged += 1;
        continue;
      }

      await Promise.all(
        duplicateRows.map((duplicate) =>
          ctx.db.delete("productSkuSearch", duplicate._id),
        ),
      );
      stats.duplicatesCollapsed += duplicateRows.length;
      stats.staleOrphansRemoved += duplicateRows.length;
      stats.unchanged += 1;
    }

    return {
      ...stats,
      continueCursor: page.continueCursor,
      isDone: page.isDone,
    };
  },
});
